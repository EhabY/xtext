package org.eclipse.xtend.lib.macro.declaration;

/**
 * A list of all modifiers that are allowed on {@link MemberDeclaration}s in
 * Xtend source code
 */
public enum Modifier {
	PUBLIC,
	PRIVATE,
	PROTECTED,
	PACKAGE,
	ABSTRACT,
	STATIC,
	DISPATCH,
	FINAL,
	STRICTFP,
	NATIVE,
	VOLATILE,
	SYNCHRONIZED,
	TRANSIENT,
	VAL,
	VAR,
	DEF,
	OVERRIDE
}
